package com.ntuc.demos.operators;

/**
 *
 * @author Sridhar Guzzu
 */
public class AssignmentOperators {

    /**
     * @param args the command line arguments
     */
    public static void main(String args[]) {
        int x = 5;
        System.out.println(x);
        x += 3;
        System.out.println(x);
        x -= 3;
        System.out.println(x);
        x *= 3;
        System.out.println(x);
        double d = 5;
        d /= 3;
        System.out.println(d);
        x %= 3;
        System.out.println(x);
    }
}
